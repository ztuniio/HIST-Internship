<?php

$num1 = 20;
$num2 = 40;
$operator = "+";

if ($operator == "+")
{
  $result = $num1 + $num2;
  echo "Result: ".$result;
}

if ($operator == "-")
{
  $result = $num1 - $num2;
  echo "Result: ".$result;
}

if ($operator == "*")
{
  $result = $num1 * $num2;
  echo "Result: ".$result;
}

if ($operator == "/")
{
  $result = $num1 / $num2;
  echo "Result: ".$result;
}

?>