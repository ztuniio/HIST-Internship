		<h2><mark>What is deep web?</mark></h2>
		<p><font color="white">The deep web, invisible web, or hidden web are parts of the <i>World Wide Web</i> whose contents are not indexed by standard web search-engine programs. This is in contrast to the "surface web", which is accessible to anyone using the Internet.</font></p>
 
<br>
                
		<h2><mark>What is dark web?</mark></h2>
		<p><font color="white">The dark web is the <i>World Wide Web</i> content that exists on darknets: overlay networks that use the Internet but require specific software, configurations, or authorization to access.</font></p>

		

</pre>
<hr />
		<h3><font color="white">What is difference between Deep web and Dark web?</font></h3>

		
                <p><font color="white">Simply put, <b>the deep web is any part of the Net that is not indexed by search engines.</b> This includes websites that gate their content behind paywalls, password-protected websites and even the contents of your email. The dark web, on the other hand, uses encryption software to provide even greater security.</font></p>


		
